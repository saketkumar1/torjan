package com.example.torjan;

public class pojo {

    int xvalue,yvalue;

    public pojo(int xvalue, int yvalue) {
        this.xvalue = xvalue;
        this.yvalue = yvalue;
    }

    public pojo(){

    }
    public int getXvalue() {
        return xvalue;
    }

    public void setXvalue(int xvalue) {
        this.xvalue = xvalue;
    }

    public int getYvalue() {
        return yvalue;
    }

    public void setYvalue(int yvalue) {
        this.yvalue = yvalue;
    }
}
